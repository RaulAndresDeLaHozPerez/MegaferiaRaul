    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String title = TextFieldTituloLibro.getText();
        String[] authorsData = jTextArea2.getText().split("\n");
        String isbn = TextFieldIsbn.getText();
        String genre = ComboBoxGeneroLibro.getItemAt(ComboBoxGeneroLibro.getSelectedIndex());
        String format = ComboBoxFormato.getItemAt(ComboBoxFormato.getSelectedIndex());
        double value = Double.parseDouble(TextFieldValorLibro.getText());
        String publisherData = ComboBoxEditorial.getItemAt(ComboBoxEditorial.getSelectedIndex());
        
        ArrayList<Author> authors = new ArrayList<>();
        for (String authorData : authorsData) {
            long authorId = Long.parseLong(authorData.split(" - ")[0]);
            for (Autor autor : this.autors) {
                if (author.getId() == authorId) {
                    authors.add(author);
                }
            }
        }
        
        String publisherNit = publisherData.split(" ")[1].replace("(", "").replace(")", "");
        
        Publisher publisher = null;
        for (Publisher publish : this.publishers) {
            if (publish.getNit().equals(publisherNit)) {
                publisher = publish;
            }
        }
        
        if (RadioBtnImpreso.isSelected()) {
            int pages = Integer.parseInt(TextFieldNroPaginas.getText());
            int copies = Integer.parseInt(TextFieldNroEjemplares.getText());
            
            this.books.add(new PrintedBook(title, authors, isbn, genre, format, value, publisher, pages, copies));
        }
        if (RadiobtnDigital.isSelected()) {
            String hyperlink = TextFieldHipervinculo.getText();
            if (hyperlink.equals("")) {
                this.books.add(new DigitalBook(title, authors, isbn, genre, format, value, publisher));
            } else {
                this.books.add(new DigitalBook(title, authors, isbn, genre, format, value, publisher, hyperlink));
            }
        }
        if (RadiobtnAudioLibro.isSelected()) {
            int duration = Integer.parseInt(TextFieldDuracion.getText());
            String[] narratorData = ComboBoxNarradorLibro.getItemAt(ComboBoxNarradorLibro.getSelectedIndex()).split(" - ");
            
            long narratorId = Long.parseLong(narratorData[0]);
            
            Narrator narrator = null;
            for (Narrator narrat : this.narrators) {
                if (narrat.getId() == narratorId) {
                    narrator = narrat;
                }
            }
            
            this.books.add(new Audiobook(title, authors, isbn, genre, format, value, publisher, duration, narrator));
        }
    }//GEN-LAST:event_jButton4ActionPerformed

